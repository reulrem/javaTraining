package P_20211201;

public class Assignment01P80 {
	public static void main(String[] args) {
		// 대입연산자에는 복합연산자, 단순연산자가 있습니다.
		// 단순연산자인 = 는 오른쪽의 값을 왼쪽에 대입한다는 명령어입니다.
		// 복합연산자는 +=, -=, %= 등이 있는데 좌측의 변수에 오른쪽 값을 기호에 따라 연산하는 명령어입니다.
		int a = 10;
		
		System.out.println(a);
		
		a+=10;
		
		System.out.println(a);
		
		a-=5;
		
		System.out.println(a);
		
		a*=3;
		
		System.out.println(a);
	
		a/=4;
		
		System.out.println(a);
		
		a %= 12;
		
		System.out.println(a);
		
		
	}
}
