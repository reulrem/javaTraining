package P_20211201;

public class AndOr01P72 {

	public static void main(String[] args) {
		// 논리 연산자
		// 2개 이상의 boolean형식의 값을 받아 계산합니다.
		// 논리식1 & 논리식2 는 양쪽 모두가 true일경우 true
		// 논리식1 | 논리식2 는 한쪽이라도 true일경우 true를 출력합니다

		System.out.println( ( 100 > 50 ) & ( 30 > 50 ));
		System.out.println( ( 100 > 50 ) | ( 30 > 50 ));
	}

}
