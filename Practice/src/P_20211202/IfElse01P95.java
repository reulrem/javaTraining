package P_20211202;

public class IfElse01P95 {
	public static void main(String[] args) {
//		if/else 구문은 if문과 그것이 거짓일때 작성하는 구문입니다.
//		if문이 참일경우 같은 구문인 else문이 생략되기때문에 연산횟수가 절약됩니다.
//		else문은 단독으로 사용될 수 없습니다.
//		if문엔 조건식이 필수로 들어가야하지만 else문은 조건식을 쓰지 않습니다.
		
		int a = 14;
		if(a == 15) {
			System.out.println("15입니다.");
		} else {
			System.out.println("15가 아닙니다.");
		}
	}
}
