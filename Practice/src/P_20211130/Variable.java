package P_20211130;

public class Variable {

	public static void main(String[] args) {
		// 변수는 일종의 박스입니다.
		// 이 박스에 명칭과 넣을 물건의 종류를 부여해야합니다.
		
		// int 는 정수를 뜻합니다. 
		// 아래 코드는 a라는 박스에 정수를 담는다는 것을 의미합니다.
		
		int a ;
		// a에 값을 넣고 출력합니다.
		a = 5;
		System.out.println(a);
		
		// 정수를 담을 수 있는 A 라는 박스를 생성하세요.
		int A ;
		// A 박스에 10을 넣어주세요.
		A = 10;
		// A 박스에 있는 값을 출력해주세요.
		System.out.println(A);
		
		//없는 변수를 호출할 순 없습니다.
		//System.out.println(number);
	}
	
}