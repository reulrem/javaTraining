package P_20211130;

public class Variable2 {

	public static void main(String[] args) {
		// 변수의 선언
		int b ,c ;// 동시에 2개 이상의 변수도 선언 가능합니다.
		b = 10; // b박스엔 10을, 
		c = 15; // c박스엔 15를 넣습니다.
		// 다음과 같이 선언과 동시에 초기화를 할 수 있습니다.
		int a = 5;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);

		a= 20;
		System.out.println(a);
	}
	
}